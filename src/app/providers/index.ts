/* Если AppRouter только конфигурирует маршруты (без логики) → app/providers
Это означает, что AppRouter отвечает только за настройку маршрутов и ничего больше:
Он не содержит бизнес-логики (например, проверку авторизации).
Он не рендерит элементы, кроме <Routes> и <Route>.
Он не управляет состоянием (например, какая страница активна).
"Только конфигурирует маршруты" = просто указывает пути и соответствующие страницы.
Если в AppRouter есть логика (авторизация, API, стейт), ее надо вынести.
FSD рекомендует хранить AppRouter.tsx в app/providers, потому что он "оборачивает" всё приложение, но не относится к фичам или страницам.

Пример:
export const AppRouter = () => {
  return (
    <BrowserRouter>
      <Routes>
        <Route path="/" element={<MainPage />} />
        <Route path="/week" element={<WeekPage />} />
        <Route path="/day/:dayId" element={<DayPage />} />
      </Routes>
    </BrowserRouter>
  );
};

Мы просто указываем маршруты и связываем их с компонентами страниц.
AppRouter ничего не знает о логине, ролях пользователя, API и т. д.
*/
